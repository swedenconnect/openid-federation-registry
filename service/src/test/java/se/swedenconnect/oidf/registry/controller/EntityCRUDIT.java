  @BeforeEach
  void setUp() {
    this.apiClient = new ApiClient();
    this.apiClient.setBasePath("http://localhost:" + this.port);

    // Configure authentication
    final HttpBearerAuth bearerAuth = (HttpBearerAuth) this.apiClient.getAuthentication("bearerAuth");
    bearerAuth.setBearerToken(this.jwtTestUtils.createJwt(JwtTestUtils.OrganisationType.PM));

    // Configure selected org number
    this.apiClient.addDefaultHeader("selected-org-number", JwtTestUtils.OrganisationType.PM.orgId);

    this.entitiesApi = new EntitiesApi(this.apiClient);
  }

  // ========== Federation Entity Tests ==========

  @Test
  void testCreateFederationEntityWithAutoGeneratedId() throws ApiException {
    // Arrange
    final FederationEntityInput input = new FederationEntityInput()
        .subject("http://www.pm.se/oidf/federation")
        .issuer("http://www.pm.se/oidf/ta")
        .metadata(Map.of("federation_entity", Map.of("organization_name", "PM")));

    // Act
    final FederationEntity created = this.entitiesApi.createFederationEntity(input);

    // Assert
    assertThat(created).isNotNull();
    assertThat(created.getEntityId()).isNotNull();
    assertThat(created.getSubject()).isEqualTo("http://www.pm.se/oidf/federation");
    assertThat(created.getIssuer()).isEqualTo("http://www.pm.se/oidf/ta");
    assertThat(created.getMetadata()).isNotNull();
  }

