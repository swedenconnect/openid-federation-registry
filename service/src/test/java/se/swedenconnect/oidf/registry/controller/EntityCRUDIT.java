  @Test
  void testEntityIsolationBetweenOrganizations() throws ApiException {
    // Arrange - Create entity with PM organization
    final UUID entityId = UUID.randomUUID();
    final FederationEntityInput input = new FederationEntityInput()
        .subject("http://www.pm.se/oidf/pm-entity")
        .issuer("http://www.pm.se/oidf/ta");
    this.entitiesApi.createFederationEntityWithId(entityId, input);

    // Act - Try to access with AF organization
    final ApiClient afApiClient = new ApiClient();
    afApiClient.setBasePath("http://localhost:" + this.port);
    final HttpBearerAuth afBearerAuth = (HttpBearerAuth) afApiClient.getAuthentication("bearerAuth");
    afBearerAuth.setBearerToken(this.jwtTestUtils.createJwt(JwtTestUtils.OrganisationType.AF));
    afApiClient.addDefaultHeader("selected-org-number", JwtTestUtils.OrganisationType.AF.orgId);
    final EntitiesApi afEntitiesApi = new EntitiesApi(afApiClient);

    // Assert - Should not be found
    assertThatThrownBy(() -> afEntitiesApi.getFederationEntity(entityId))
        .isInstanceOf(ApiException.class)
        .satisfies(exception -> {
          final ApiException apiException = (ApiException) exception;
          assertThat(apiException.getCode()).isEqualTo(404);
        });
  }
